### LIST
my_favorite_desserts = ['cookies', 'cake', 'Ice Cream', 'Donuts'] # list of strings
print(my_favorite_desserts)


#list of integers

bunnies_spotted = [3, 5, 2, 8, 5, 4 ]
print(bunnies_spotted)

#list of Booleans

robot_answers = [True, False, False, True, True]
print(robot_answers)
### mix of string, integers, booleans

mix_of_all = ['tree', 4, True, 'cars', 'books', 5]
print(mix_of_all)

### Lists are ordered

citrus_fruits = ['orange', 'lemon', 'grapefruit', 'lime']
more_citrus_fruits = ['orage', 'grapefruit', 'lime', 'lemon']

fruits = citrus_fruits == more_citrus_fruits

print(fruits)



### Lists can be accessed with index

citrus_fruits = ['orange', 'lemon', 'grapefruit', 'lime']

print(citrus_fruits[2])

print(citrus_fruits[0])

##List can be sliced



citrus_fruits = ['orange', 'lemon', 'grapefruit', 'lime']

print(citrus_fruits[1:3])

print(citrus_fruits[0:2])




























